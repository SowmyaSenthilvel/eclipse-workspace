import java.util.*;
public class ArrayListDemo {
	public static void main(String[] args) {
	//To initialize Arraylist
	ArrayList<Integer> alist=new ArrayList<Integer>();
	//To add one element to arraylist 
	alist.add(8);
	alist.add(100);
	alist.add(12);
	System.out.println(alist);
	//To remove one element from Arraylist
	alist.remove(2);
	System.out.println(alist);
	//To add multiple elements to Arraylist
	ArrayList<Integer> list=new ArrayList<Integer>(Arrays.asList(7,8,10,12,-9,8,45));
	System.out.println(list);
	//To add extra elements to the ezisting arraylist
	Collections.addAll(list, 1,2,3);
	System.out.println(list);
	//To sort the existing elememts in the arraylist 
	Collections.sort(list);
	System.out.println(list);
	//We can change the elements in arraylist
	alist.set(0,90);
	System.out.println(alist);
	}
	
}
