package connecting_MySql;

public class StudentClasss {
	int StudentID;
	String Stud_Name;
	int Stud_Age;
	long Mobile_Num;
	String Email_ID;
	
	public int getStudentID() {
		return StudentID;
	}
	public void setStudentID(int studentID) {
		StudentID = studentID;
	}
	public String getStud_Name() {
		return Stud_Name;
	}
	public void setStud_Name(String stud_Name) {
		Stud_Name = stud_Name;
	}
	public int getStud_Age() {
		return Stud_Age;
	}
	public void setStud_Age(int stud_Age) {
		Stud_Age = stud_Age;
	}
	public long getMobile_Num() {
		return Mobile_Num;
	}
	public void setMobile_Num(long mobile_Num) {
		Mobile_Num = mobile_Num;
	}
	public String getEmail_ID() {
		return Email_ID;
	}
	public void setEmail_ID(String email_ID) {
		Email_ID = email_ID;
	}
	

}
